                    In our research, we've seen that Javascript is a great tool for displaying a web application's user workflow. With HTML, users typically must access multiple screens to complete a web application process,
                </p>
                <p class="hidden">
                    with little to no control over the order of these steps. With Javascript, developers can display all of the users' necessary steps on a single screen, giving users a "big picture" view of their workflow.
                    To illustrate, Volkswagen's web site lets users configure their own car using the function implemented in HTML. Users can specify the type of engine, the color of the car's exterior and interior, and optional features.

                    To configure a car, the interface requires users to click through five different screens. The problem is that users can't see how many steps are ahead of them. Users also don't know if they can easily go back and change their specifications. Choosing an interior trim that costs extra might be acceptable early in the process, but when a more preferable option comes later, users may want to change their interior again to reduce costs.

                    With the HTML application, users cannot tell if changing preferences will be a simple process or not, thus adding an element of uncertainty to the experience and increasing their chances that they will not complete the transaction.

                    In contrast, the developers of Volkswagen's Asia Pacific application created aÂ car configuratorimplemented in Javascript. The configurator matches the functionality of the HTML implementation, but it lets users choose all specifications on one screen. As a result, users can drag and drop their specifications into a template to immediately see the results of their actions.

                    (We do recognize that pure javascript-based application is not perfect. If we tested this application, we would expect to see some users exhibit problems with the design's novel interface. However, it does a good job of demonstrating Javascript ability to display an entire user workflow on one screen, something not possible in HTML.)

                    In addition to the entire process being accessible on one screen, users get immediate responses to their specifications, without having to wait for new pages to load.
