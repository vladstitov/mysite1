                    To illustrate, Design Center's applicationÂ lets users build their own product on one screen. Users can design their own kitchen by choosing the type of faucet and the colors of the sink, countertop, cabinets, walls and windows.

                    In addition to viewing the entire workflow on one screen, users have control over the order they specify preferences, something not easy to implement in HTML. As soon as users specify the criteria, the visual display reflects their choices.

                    While there are ways to create the Moen implementation with JavaScript and HTML, it's very clumsy and involves a great deal of download time. It would require a JavaScript function that replaces one image, or choice, with another, based on the colors selected. For Moen's developers to do this with JavaScript, they would need a separate picture of the kitchen with every possible color/sink combination. This becomes very difficult for developers to manage.

